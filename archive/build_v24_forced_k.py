    "class DeepfakeDataset(Dataset):\n",
    "    def __init__(self, real_dir, fake_dir, transform=None):\n",
    "        self.r = [os.path.join(real_dir, f) for f in os.listdir(real_dir) if f.endswith(('.jpg', '.png'))] if os.path.exists(real_dir) else []\n",
    "        self.f = [os.path.join(fake_dir, f) for f in os.listdir(fake_dir) if f.endswith(('.jpg', '.png'))] if os.path.exists(fake_dir) else []\n",
    "        self.all_files = self.r + self.f\n",
    "        self.labels = [0]*len(self.r) + [1]*len(self.f)\n",
    "        self.transform = transform\n",
    "    def __len__(self): return len(self.all_files)\n",
    "    def __getitem__(self, idx):\n",
    "        try:\n",
    "            img = Image.open(self.all_files[idx]).convert('RGB')\n",
    "            if self.transform: img = self.transform(img)\n",
    "            return img, self.labels[idx], self.all_files[idx]\n",
    "        except: return torch.zeros((3, RESOLUTION, RESOLUTION)), self.labels[idx], self.all_files[idx]\n",
    "\n",
