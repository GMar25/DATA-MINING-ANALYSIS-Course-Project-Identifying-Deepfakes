    "        self.all_files = self.real_files + self.fake_files\n",
    "        self.labels = [0]*len(self.real_files) + [1]*len(self.fake_files)\n",
    "        self.transform = transform\n",
    "    def __len__(self): return len(self.all_files)\n",
    "    def __getitem__(self, idx):\n",
    "        try:\n",
    "            image = Image.open(self.all_files[idx]).convert('RGB')\n",
    "            if self.transform: image = self.transform(image)\n",
    "            return image, self.labels[idx], self.all_files[idx]\n",
    "        except: return torch.zeros((3, RESOLUTION, RESOLUTION)), self.labels[idx], self.all_files[idx]\n",
    "\n",
    "full_dataset = DeepfakeDataset(REAL_IMAGE_DIR, FAKE_IMAGE_DIR, transform=transforms.Compose([\n",
